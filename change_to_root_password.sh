#!/bin/bash

# ตรวจสอบว่าถูกเรียกใช้ด้วยสิทธิ์ root หรือไม่
if [ "$(id -u)" != "0" ]; then
   echo "คุณต้องใช้สิทธิ์ root เพื่อเปลี่ยนรหัสผ่าน" 1>&2
   exit 1
fi

# กำหนดผู้ใช้ที่จะเปลี่ยนรหัสผ่าน
user=$(whoami)

# เปลี่ยนรหัสผ่านของผู้ใช้เป็น "root"
echo "$user:root" | chpasswd

if [ $? -eq 0 ]; then
    echo "รหัสผ่านของผู้ใช้ $user ถูกเปลี่ยนเป็น 'root' เรียบร้อยแล้ว"
else
    echo "เกิดข้อผิดพลาดในการเปลี่ยนรหัสผ่าน" 1>&2
    exit 1
fi
